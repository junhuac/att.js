module('phoneNumber');

test("Basic init and event tests", function() {
    // first column is input.
    // second column is result when parsed.
    // third column is result when stringified.
    var testNumbers = [
            ['1-800-Pick-UPS', '18007425877', '1 (800) 742-5877'],
            ['1-800-GOT-JUNK', '18004685865', '1 (800) 468-5865'],
            ['2345678910', '2345678910', '(234) 567-8910'],
            ['123412341231234', '123412341231234', '123412341231234'],
            ['+1(234)754-1345', '12347541345', '1 (234) 754-1345'],
            ['555.234.1234', '5552341234', '(555) 234-1234'],
            ['3', '3', '3'],
            ['32', '32', '32'],
            ['323', '323', '323'],
            ['3234', '3234', '323-4'],
            ['32343', '32343', '323-43'],
            ['323434', '323434', '323-434'],
            ['3234346', '3234346', '323-4346'],
            ['32343467', '32343467', '(323) 434-67'],
            ['323434678', '323434678', '(323) 434-678'],
            ['3234346789', '3234346789', '(323) 434-6789'],
            ['32343467893', '32343467893', '32343467893'],
            ['1', '1', '1'],
            ['19', '19', '1 (9  ) '],
            ['190', '190', '1 (90 ) '],
            ['1909', '1909', '1 (909) '],
            ['19097', '19097', '1 (909) 7'],
            ['190975', '190975', '1 (909) 75'],
            ['1909754', '1909754', '1 (909) 754'],
            ['19097543', '19097543', '1 (909) 754-3'],
            ['1909754394', '1909754394', '1 (909) 754-394'],
            ['19097543942', '19097543942', '1 (909) 754-3942'],
            ['190975439421', '190975439421', '190975439421']
        ],
        input,
        parsed,
        stringified;

    for (var i = 0, l = testNumbers.length; i < l; i++) {
        input = testNumbers[i][0];
        parsed = testNumbers[i][1];
        stringified = testNumbers[i][2];

        equal(ATT.phoneNumber.parse(input), parsed);
        equal(ATT.phoneNumber.stringify(input), stringified);
    }
});
